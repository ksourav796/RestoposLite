package com.hyva.restopos.rest.pojo;

import lombok.Data;

import javax.persistence.Temporal;
import java.math.BigDecimal;
import java.util.Date;
@Data
public class AgentPojo {
    private Long agentId;
    private String agentName;
    private String accountNo;
    private String agentAccountCode;
    private String email;
    private String mobile;
    private String address;
    private BigDecimal commission;
    @Temporal(javax.persistence.TemporalType.DATE)
    private Date effectiveDate;
    private String status;
    private String gstinNo;
    private String ecommerce;
    private String locationId;
    private String useraccount_id;
    private String invoiceNO;
    private Date invdate;
    private double totalAmount;
    private double totalCommission;
    public AgentPojo(Long agentId, String agentName, String accountNo, String agentAccountCode, String email, BigDecimal commission, String invoiceNO, Date invdate, double totalAmount,double totalCommission) {
        this.agentId = agentId;
        this.agentName = agentName;
        this.accountNo = accountNo;
        this.agentAccountCode = agentAccountCode;
        this.email = email;
        this.commission = commission;
        this.invoiceNO = invoiceNO;
        this.invdate = invdate;
        this.totalAmount = totalAmount;
        this.totalCommission = totalCommission;

    }
    public AgentPojo() {
    }


    public Long getAgentId() {
        return agentId;
    }

    public void setAgentId(Long agentId) {
        this.agentId = agentId;
    }

    public String getAgentName() {
        return agentName;
    }

    public void setAgentName(String agentName) {
        this.agentName = agentName;
    }

    public String getAccountNo() {
        return accountNo;
    }

    public void setAccountNo(String accountNo) {
        this.accountNo = accountNo;
    }

    public String getAgentAccountCode() {
        return agentAccountCode;
    }

    public void setAgentAccountCode(String agentAccountCode) {
        this.agentAccountCode = agentAccountCode;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getMobile() {
        return mobile;
    }

    public void setMobile(String mobile) {
        this.mobile = mobile;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public BigDecimal getCommission() {
        return commission;
    }

    public void setCommission(BigDecimal commission) {
        this.commission = commission;
    }

    public Date getEffectiveDate() {
        return effectiveDate;
    }

    public void setEffectiveDate(Date effectiveDate) {
        this.effectiveDate = effectiveDate;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public String getGstinNo() {
        return gstinNo;
    }

    public void setGstinNo(String gstinNo) {
        this.gstinNo = gstinNo;
    }

    public String getEcommerce() {
        return ecommerce;
    }

    public void setEcommerce(String ecommerce) {
        this.ecommerce = ecommerce;
    }

    public String getLocationId() {
        return locationId;
    }

    public void setLocationId(String locationId) {
        this.locationId = locationId;
    }

    public String getUseraccount_id() {
        return useraccount_id;
    }

    public void setUseraccount_id(String useraccount_id) {
        this.useraccount_id = useraccount_id;
    }
}
