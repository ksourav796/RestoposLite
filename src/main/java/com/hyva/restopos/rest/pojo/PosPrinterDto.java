package com.hyva.restopos.rest.pojo;

import lombok.Data;

@Data
public class PosPrinterDto {

    private Long id;
    private String posPrinter;
    private String user;
    private String tableConfig;

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getPosPrinter() {
        return posPrinter;
    }

    public void setPosPrinter(String posPrinter) {
        this.posPrinter = posPrinter;
    }

    public String getUser() {
        return user;
    }

    public void setUser(String user) {
        this.user = user;
    }

    public String getTableConfig() {
        return tableConfig;
    }

    public void setTableConfig(String tableConfig) {
        this.tableConfig = tableConfig;
    }
}
