package com.hyva.restopos.rest.pojo;

import lombok.Data;

@Data
public class UrbanItemPojo {
    private String item_name;
    private Long item_quantity;
    private double item_unit_price;
    private double item_final_price;
    String foodType;
    String imgUrl;
    String taxes;
    String charges;
    double discount;

    public String getCharges() {
        return charges;
    }

    public void setCharges(String charges) {
        this.charges = charges;
    }

    public void setDiscount(double discount) {
        this.discount = discount;
    }

    public double getDiscount() {
        return discount;
    }

    public String getImgUrl() {
        return imgUrl;
    }

    public void setImgUrl(String imgUrl) {
        this.imgUrl = imgUrl;
    }

    public String getTaxes() {
        return taxes;
    }

    public void setTaxes(String taxes) {
        this.taxes = taxes;
    }

    public String getFoodType() {
        return foodType;
    }

    public void setFoodType(String foodType) {
        this.foodType = foodType;
    }

    public String getItem_name() {
        return item_name;
    }

    public void setItem_name(String item_name) {
        this.item_name = item_name;
    }

    public Long getItem_quantity() {
        return item_quantity;
    }

    public void setItem_quantity(Long item_quantity) {
        this.item_quantity = item_quantity;
    }

    public double getItem_unit_price() {
        return item_unit_price;
    }

    public void setItem_unit_price(double item_unit_price) {
        this.item_unit_price = item_unit_price;
    }

    public double getItem_final_price() {
        return item_final_price;
    }

    public void setItem_final_price(double item_final_price) {
        this.item_final_price = item_final_price;
    }
}
